// Mapped separately due to varying name conventions in raw data
const _alessandraNascimentoImg = img_nascimento_alessandra;

// Build speakers map
const speakersMap = new Map<string, Speaker>();

rawEntries.forEach((entry) => {
  const key = entry.name.replace(/\s+/g, " ").trim();
  if (!speakersMap.has(key)) {
    speakersMap.set(key, {
      id: key
        .toLowerCase()
        .replace(/[^a-z0-9]/g, "-")
        .replace(/-+/g, "-"),
      name: entry.name,
      position: entry.position,
      institution: entry.institution,
      profileLink: entry.profileLink,
      image:
        speakerImages[key] ??
        (entry.profileLink?.includes("alessandra-nascimento")
          ? _alessandraNascimentoImg
          : undefined),
      subspecialties: [],
      lectures: [],
    });
  }
  const speaker = speakersMap.get(key)!;
  // Update position/institution/profileLink if current is empty
  if (!speaker.position && entry.position) speaker.position = entry.position;
  if (!speaker.institution && entry.institution)
    speaker.institution = entry.institution;
  if (!speaker.profileLink && entry.profileLink)
    speaker.profileLink = entry.profileLink;

  if (!speaker.subspecialties.includes(entry.subspecialty)) {
    speaker.subspecialties.push(entry.subspecialty);
  }

  speaker.lectures.push({
    title: entry.lectureTitle,
    date: entry.date,
    youtubeId: extractYoutubeId(entry.youtubeUrl),
    subspecialty: entry.subspecialty,
    hostInstitute: entry.hostInstitute,
  });
});

export const speakers: Speaker[] = Array.from(speakersMap.values()).sort(
  (a, b) => a.name.localeCompare(b.name),
);

export const allSpeakerSubspecialties = Array.from(
  new Set(speakers.flatMap((s) => s.subspecialties)),
).sort();

export const allInstitutions = Array.from(
  new Set(speakers.map((s) => s.institution).filter(Boolean)),
).sort();
